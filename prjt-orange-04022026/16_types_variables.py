# Exemples de différents types de variables

# Entier (int)
age = 25
print("La variable 'age' est:", age)
print("Type de 'age':", type(age))
print("-" * 20)

# Nombre à virgule flottante (float)
prix = 19.99
print("La variable 'prix' est:", prix)
print("Type de 'prix':", type(prix))
print("-" * 20)

# Chaîne de caractères (str)
nom = "Python"
print("La variable 'nom' est:", nom)
print("Type de 'nom':", type(nom))
print("-" * 20)

# Booléen (bool)
est_actif = True
print("La variable 'est_actif' est:", est_actif)
print("Type de 'est_actif':", type(est_actif))
