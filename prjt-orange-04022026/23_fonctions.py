# Définition de la fonction avec un paramètre 'nom'
def saluer(nom):
    # Affiche un message de bienvenue personnalisé
    print(f"Bonjour {nom}, bienvenue dans le chapitre des fonctions !")

# Demande le nom à l'utilisateur
nom_utilisateur = input("Entrez votre nom : ")

# Appel de la fonction avec la valeur saisie
saluer(nom_utilisateur)
